function verificar() {

    var data = new Date()
    var ano = data.getFullYear()
    var fano = document.getElementById('txtano')
    var res = document.getElementById('res')

    if(fano.value.length == 0 || Number(fano.value) > ano) {
        window.alert("Verifique os dados e tente novamente!")
    } else {
        var fsex = document.getElementsByName('radsex')
        var idade = ano - Number(fano.value)
        var genero = ''

        // criação de img de forma dinâmica
        var img = document.createElement('img')
        img.setAttribute('id', 'foto')


        if (fsex[0].checked) {
            genero = 'Homem'

            if(idade >=0 && idade < 10) {
                //criança
                img.setAttribute('src','homem1.png')
            } else if (idade < 21) {
                //jovem
                img.setAttribute('src', 'homem2.png')
            } else if (idade < 50) {
                //adulto
                img.setAttribute('src', 'homem3.png')
            } else {
                //idoso
                img.setAttribute('src', 'homem4.png')
            }

        } else if (fsex[1].checked) {
            genero = 'Mulher'

            if(idade >=0 && idade < 10) {
                //criança
                img.setAttribute('src', 'mulher1.png')
            } else if (idade < 21) {
                //jovem
                img.setAttribute('src', 'mulher2.png')
            } else if (idade < 50) {
                //adulto
                img.setAttribute('src', 'mulher3.png')
            } else {
                //idoso
                img.setAttribute('src', 'mulher4.png')
            }
        }
        res.style.textAlign = 'center' //alinhamento com js
        res.innerHTML = `Detectamos ${genero} com ${idade} anos`
        res.appendChild(img)
    }
}